#include "libft.h"

void	ft_putnbr(int n)
{
	long s;

	s = (long)n;
	if (s < 0)
	{
		ft_putchar('-');
		s = -s;
	}
	if (s >= 10)
	{
		ft_putnbr(s / 10);
		ft_putnbr(s % 10);
	}
	else
		ft_putchar(s + '0');
}
