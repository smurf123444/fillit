#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*mapped;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	mapped = (char*)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (mapped == NULL)
		return (NULL);
	while (s[i])
	{
		mapped[i] = f(i, s[i]);
		i++;
	}
	mapped[i] = '\0';
	return (mapped);
}
